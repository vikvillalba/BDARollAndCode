    public Integer obtenerCantidad(){
         if(txtCantidadStock.getText().isBlank() || txtCantidadStock.getText().isEmpty()){
           try {
               throw new PresentacionException("La cantidad de stock no puede estar vac√≠a.");
           } catch (PresentacionException ex) {
               txtCantidadStock.setText("");
               JOptionPane.showMessageDialog(null, ex.getMessage(),"Aviso", JOptionPane.INFORMATION_MESSAGE);
           }
       }
        return Integer.valueOf(this.txtCantidadStock.getText());
    }
